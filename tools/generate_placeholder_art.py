# --- Palette ---
CREAM_MIST    = (246, 241, 232)  # bg / sky
PASTEL_MINT   = (191, 216, 194)  # mid env
LAVENDER_BLUE = (159, 175, 214)  # foreground / characters
MUTED_INK     = (74,  78,  105)  # outline / UI text
SOFT_PEACH    = (242, 184, 160)
BUTTER_YELLOW = (246, 226, 127)
DUSTY_ROSE    = (217, 165, 179)
PALE_TEAL     = (158, 214, 211)
EVENING_LILAC = (125, 127, 166)
COOL_SLATE    = (92,  96,  122)

T = None  # transparent

def make_image(w, h, pixels, palette_map):
    """Create an image from a 2D grid of palette keys."""
    img = Image.new("RGBA", (w, h), (0, 0, 0, 0))
    for y, row in enumerate(pixels):
        for x, key in enumerate(row):
            if key is not None and key in palette_map:
                img.putpixel((x, y), palette_map[key] + (255,))
    return img

def save(img, path):
    os.makedirs(os.path.dirname(path), exist_ok=True)
    img.save(path)
    print(f"  {path}")


